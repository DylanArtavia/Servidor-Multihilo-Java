    /**
     * Agrega una línea de texto al registro histórico. Incluye fecha y hora
     * para facilitar diagnóstico. Se realiza en el hilo de la interfaz para
     * evitar problemas con la actualización visual.
     */
    public void appendLog(String texto) {
        javax.swing.SwingUtilities.invokeLater(() -> {
            txtLog.append("["
                    + java.time.LocalDateTime.now()
                            .format(java.time.format.DateTimeFormatter
                                    .ofPattern("yyyy-MM-dd HH:mm:ss"))
                    + "] " + texto + "\n");

            txtLog.setCaretPosition(txtLog.getDocument().getLength());
        });
    }

    /**
     * Guarda el registro de eventos en un archivo de texto. No interrumpe el
     * funcionamiento del programa si ocurre un error.
     */
    public void saveLogToFile(File f) {
        try (java.io.FileWriter fw = new java.io.FileWriter(f, true)) {
            fw.write(txtLog.getText());
            fw.flush();
        } catch (Exception e) {
            // Se ignoran errores para evitar bloquear el sistema.
        }
    }

