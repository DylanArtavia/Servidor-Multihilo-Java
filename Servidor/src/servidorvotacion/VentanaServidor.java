    private void btnLevantarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLevantarActionPerformed
        // TODO add your handling code here:
        /**
         * Inicia el servidor si aún no está en ejecución. Cambia el estado
         * visual y registra el evento.
         */

        //servidor = new Servidor(this);
        //servidor.iniciar();
        if (servidor == null) {
            servidor = new Servidor(this); // la clase Servidor que ya tienes
            servidor.iniciar();
            lblEstado.setText("Servidor corriendo");
        } else {
            appendLog("Servidor ya creado.");
        }

    }//GEN-LAST:event_btnLevantarActionPerformed

    private void btnDetenerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetenerActionPerformed
        // TODO add your handling code here:
        /**
         * Detiene el servidor si se encontraba activo. Limpia la referencia y
         * actualiza el estado visual.
         */
        //if (servidor != null) servidor.detener();
        if (servidor != null) {
            servidor.detener();
            servidor = null;
            lblEstado.setText("Servidor detenido");
        } else {
            appendLog("Servidor no está activo.");
        }
    }//GEN-LAST:event_btnDetenerActionPerformed

    private void btnMinarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMinarActionPerformed
        // TODO add your handling code here:
        /**
         * Ejecuta el proceso de minado en un hilo separado para no congelar la
         * interfaz. Requiere servidor activo.
         */
        /* if (servidor != null) {
            Minador.minar(servidor, this);
        }*/
        if (servidor == null) {
            appendLog("Inicia el servidor primero.");
            return;
        }
        // Ejecutar en hilo para no bloquear la GUI
        new Thread(() -> {
            appendLog("Inicio minado (puede tardar)...");
            Minador.minar(servidor, this); // la clase Minador que ańadiremos
        }).start();
    }//GEN-LAST:event_btnMinarActionPerformed

    private void btnResultadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResultadosActionPerformed
        // TODO add your handling code here:
        /**
         * Abre una ventana nueva con los resultados actuales. Si el servidor no
         * está encendido, igual intenta mostrarlos en caso de que existan datos
         * cargados previamente.
         */
        //new VentanaResultados(servidor).setVisible(true);
        if (servidor == null) {
            appendLog("Servidor no iniciado. Aun así puedes ver resultados (si hay datos guardados).");
        }
        VentanaResultados vr = new VentanaResultados(servidor);
        vr.setVisible(true);
    }//GEN-LAST:event_btnResultadosActionPerformed

